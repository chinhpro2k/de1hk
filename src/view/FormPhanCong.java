    private Laixe getLaiXeWithMa(int ma) {
        List<Laixe> listlx = IOFile.doc("src/controller/laixe.dat");
        for (Laixe i : listlx) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

    private Tuyen getTuyenWithMa(int ma) {
        List<Tuyen> listlx = IOFile.doc("src/controller/tuyen.dat");
        for (Tuyen i : listlx) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

    private boolean check(int malx, int matuyen) {
        int r = tm.getRowCount();
        for (int i = 0; i < r ; i++) {
            if (Integer.parseInt(tm.getValueAt(i, 0).toString()) == malx
                    && Integer.parseInt(tm.getValueAt(i, 2).toString()) == matuyen) {
                return false;
            }
        }
        return true;
    }

