    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        List<PhanCong> list = new ArrayList<>();
        for (int i = 0; i < tm.getRowCount(); i++) {
           int malx=Integer.parseInt(tm.getValueAt(i, 0).toString());
            int matuyen=Integer.parseInt(tm.getValueAt(i, 2).toString());
             int soluot=Integer.parseInt(tm.getValueAt(i, 3).toString());
             PhanCong p=new PhanCong(getLaiXeWithMa(malx), getTuyenWithMa(matuyen), soluot);
             list.add(p);
        }
        IOFile.viet(file, list);
        System.out.println("luu thanh cong");
    }//GEN-LAST:event_btnSaveActionPerformed

