    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        List<PhanCong> list=IOFile.doc(file);
        list.sort(new Comparator<PhanCong>(){
            @Override
            public int compare(PhanCong o1,PhanCong o2){
                String t1=o1.getLaixe().getTen();
         
                 String t2=o2.getLaixe().getTen();
              
                 String ten1=t1.substring(t1.lastIndexOf(" ")+1)+t1;
                 
                 String ten2=t2.substring(t2.lastIndexOf(" ")+1)+t2;
                 
                 return ten1.compareToIgnoreCase(ten2);
            }
        });
        tm.setRowCount(0);
        for(PhanCong i:list){
            tm.addRow(i.toObject());
        }
    }//GEN-LAST:event_jButton1ActionPerformed


