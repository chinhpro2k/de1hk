    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        List<Tuyen> list=new ArrayList<>();
        for (int i = 0; i < tm.getRowCount(); i++) {
           int ma=Integer.parseInt(tm.getValueAt(i, 0).toString());
           int khoangcach=Integer.parseInt(tm.getValueAt(i, 1).toString());
            int sodiemdung=Integer.parseInt(tm.getValueAt(i, 2).toString());
           
          Tuyen t=new Tuyen(ma, khoangcach, sodiemdung);
             list.add(t);
           
        }
        IOFile.viet(file, list);
        System.out.println("luu thanh cong");
    }//GEN-LAST:event_btnSaveActionPerformed

