    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        buttonFalse();
        them=true;
        File f=new File(file);
        if (f.exists()) {
            List<Tuyen> list=IOFile.doc(file);
            if (list.size()>0) {
                Tuyen.setSma(list.get(list.size()-1).getMa()+1);
            }
        }
        jTextField1.setText(Tuyen.getSma()+"");
        jTextField2.requestFocus();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (them) {
            Tuyen l=new Tuyen();
//            l.setMa(Integer.parseInt(jTextField1.getText()));
            l.setKc(Integer.parseInt(jTextField2.getText()));
            l.setSoDiemDung(Integer.parseInt(jTextField4.getText()));
            tm.addRow(l.toObject());
            buttonTrue();
            them=false;
        }
        if (sua) {
            int r=jTable1.getSelectedRow();
            tm.setValueAt(jTextField1.getText(), r, 0);
             tm.setValueAt(jTextField2.getText(), r, 1);
              tm.setValueAt(jTextField4.getText(), r, 2);
              buttonTrue();
              sua=false;
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

